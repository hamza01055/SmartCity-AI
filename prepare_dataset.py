"""
prepare_dataset.py — Convert your raw dataset into YOLOv8 training format.

What it does:
1. Filters multi-class labels to keep only the classes you care about
2. Remaps class IDs to 0, 1, 2... (YOLO requires sequential IDs starting at 0)
3. Splits images into train/ (80%) and valid/ (20%) folders
4. Generates data.yaml ready for YOLOv8 training

USAGE:
    python prepare_dataset.py

Edit CLASS_MAP below to control which source class IDs map to which output class.
"""
import os
import random
import shutil
from pathlib import Path

# ──────────────────────────────────────────────────────────────────────────────
# CONFIG — edit these to add more classes later
# ──────────────────────────────────────────────────────────────────────────────

# Where your raw dataset lives (relative to this script's location)
RAW_ROOT = Path("data/raw_dataset")

# Where the clean YOLOv8-ready dataset will be written
OUTPUT_ROOT = Path("data/yolo_ready")

# Map: (source_folder, original_class_id) → (output_class_id, output_class_name)
# Right now we only train on potholes. Class ID 3 in your raw labels = pothole.
# To add more classes later, just append more rows here.
CLASS_MAP = {
    ("pothole", 3): (0, "Pothole"),
    ("traffic_lights", 1): (1, "Traffic_Light"),
    ("waste_container", 2): (2, "Waste_Container"),
}

# Train/validation split
VAL_RATIO = 0.2
RANDOM_SEED = 42

# ──────────────────────────────────────────────────────────────────────────────


def main():
    random.seed(RANDOM_SEED)

    # 1. Discover output class names (in order of output ID)
    classes = {}
    for (_, _src_id), (out_id, name) in CLASS_MAP.items():
        classes[out_id] = name
    class_names = [classes[i] for i in sorted(classes.keys())]
    print(f"→ Target classes: {class_names}")

    # 2. Prepare output directories
    if OUTPUT_ROOT.exists():
        shutil.rmtree(OUTPUT_ROOT)
    for split in ("train", "valid"):
        (OUTPUT_ROOT / "images" / split).mkdir(parents=True, exist_ok=True)
        (OUTPUT_ROOT / "labels" / split).mkdir(parents=True, exist_ok=True)

    # 3. Collect all (image, label) pairs grouped by source folder
    pairs = []
    src_folders = sorted({f for (f, _) in CLASS_MAP.keys()})
    for folder in src_folders:
        img_dir = RAW_ROOT / folder / "images"
        lbl_dir = RAW_ROOT / folder / "labels"
        if not img_dir.exists():
            print(f"⚠ Skipping {folder}: {img_dir} not found")
            continue

        for img_path in sorted(img_dir.iterdir()):
            if img_path.suffix.lower() not in {".jpg", ".jpeg", ".png"}:
                continue
            label_path = lbl_dir / (img_path.stem + ".txt")
            pairs.append((folder, img_path, label_path))

    if not pairs:
        print("✗ No images found. Check RAW_ROOT path.")
        return

    print(f"→ Found {len(pairs)} total images across {len(src_folders)} folder(s)")

    # 4. Shuffle and split
    random.shuffle(pairs)
    n_val = int(len(pairs) * VAL_RATIO)
    splits = {"valid": pairs[:n_val], "train": pairs[n_val:]}
    print(f"→ Split: {len(splits['train'])} train / {len(splits['valid'])} valid")

    # 5. Process each pair: filter+remap labels, copy image
    stats = {"kept": 0, "no_target_labels": 0, "boxes_kept": 0, "boxes_dropped": 0}

    for split, items in splits.items():
        for folder, img_path, label_path in items:
            kept_lines = []
            if label_path.exists():
                with open(label_path) as f:
                    for line in f:
                        parts = line.strip().split()
                        if len(parts) != 5:
                            continue
                        src_id = int(parts[0])
                        key = (folder, src_id)
                        if key in CLASS_MAP:
                            out_id, _ = CLASS_MAP[key]
                            kept_lines.append(f"{out_id} {' '.join(parts[1:])}")
                            stats["boxes_kept"] += 1
                        else:
                            stats["boxes_dropped"] += 1

            # Skip images that have no target-class boxes (no point training on them)
            if not kept_lines:
                stats["no_target_labels"] += 1
                continue

            # Unique output name to avoid collisions across source folders
            out_stem = f"{folder}_{img_path.stem}"
            out_img = OUTPUT_ROOT / "images" / split / (out_stem + img_path.suffix.lower())
            out_lbl = OUTPUT_ROOT / "labels" / split / (out_stem + ".txt")
            shutil.copy2(img_path, out_img)
            out_lbl.write_text("\n".join(kept_lines))
            stats["kept"] += 1

    # 6. Write data.yaml
    yaml_path = OUTPUT_ROOT / "data.yaml"
    yaml_content = f"""# Auto-generated by prepare_dataset.py
path: {OUTPUT_ROOT.resolve()}
train: images/train
val: images/valid

nc: {len(class_names)}
names: {class_names}
"""
    yaml_path.write_text(yaml_content)

    # 7. Summary
    print()
    print("─" * 50)
    print(f"✓ Done!")
    print(f"  Images kept:        {stats['kept']}")
    print(f"  Images skipped:     {stats['no_target_labels']} (no target-class boxes)")
    print(f"  Bounding boxes kept:    {stats['boxes_kept']}")
    print(f"  Bounding boxes dropped: {stats['boxes_dropped']} (non-target classes)")
    print(f"  Output:             {OUTPUT_ROOT.resolve()}")
    print(f"  data.yaml:          {yaml_path.resolve()}")
    print("─" * 50)
    print()
    print("Next: zip the output folder and upload to Colab")
    print(f"  cd {OUTPUT_ROOT.parent}")
    print(f"  zip -r yolo_ready.zip {OUTPUT_ROOT.name}")


if __name__ == "__main__":
    main()
